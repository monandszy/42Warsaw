#include "ft_printf.h"

size_t count_args(char *f)
{
	size_t c;

	c = 0;
    while (*f)
    {
        if (*f == '%')
        {
			c++;
			while(*f++)
				if (!((*f == '#' || *f == '0' || *f == '-' || *f == ' ' || *f == '+')
					|| (*f == '.')))
					break ;
			if (!*f)
				return (c);
        }
        f++;
    }
    return (c);
}

